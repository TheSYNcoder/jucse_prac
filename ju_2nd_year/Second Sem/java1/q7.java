import java.util.*;


class Student{
    String name;
    int roll;
    int score;
    Student( String name , int roll , int score){
        this.name = name;
        this.roll = roll;
        this.score = score;
    }
    void setname( String data){
        name = data;
    }
    void setroll( int data){
        roll = data;
    }
    void setscore( int data){
        score = data;
    }
    void showname(String data){
        System.out.println(data);
    }
    void showroll(int data){
        System.out.println(data);
    }
    void showscore(int data){
        System.out.println(data);
    }
    
    void copy( Student s) {
        this.name = s.name;
        this.roll = s.roll;
        this.score = s.score;
    }
    public static void main(String args[]){
        Student t = new Student("wow" , 3, 69);
        Student s = new Student("hhd" ,45, 343);
        System.out.println(t.name);
        t.copy(s);
        System.out.println(t.name);
        t.setname("erty");
        System.out.println(t.name);
        
    }

}
